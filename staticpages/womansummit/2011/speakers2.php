                  						<div class="speakers_speak">
                        <h2><a name="Smriti" >Smriti Irani<br />
                        </a></h2>
                        <div class="strap"> Actor, Producer and President, Women's Wing, BJP </div>
                        <div class="session"><b>SESSION: THE BATTLE OF THE SEXES</b></div>
                        <div class="fulltext">

						<img id="dyn_img_gallery" src="images/smiti.jpg" style="float: left;" height="110" align="middle" border="1" width="120">


Television actor, producer and politician, also known popularly as Tulsi, after the character she played in the cult soap opera <em>Kyunki Saas Bhi Kabhi Bahu Thi. </em>For many she is the epitome of a traditional Indian woman, a part reason why Irani was instrumental in creating a niche for the female audience in the country. In 2003, she joined the&nbsp;Bhartiya Janta Party to contest Lok Sabha elections and although she did not win, she continues to be the Secretary of BJP Maharashtra and a member of the Maharashtra Executive. An avid social worker, she runs an NGO under the name People for Change, which provides free drinking water to the poor in remote areas of the country . <br>
						</div>
                        </div>
                        <div class="speakers_speak">
                        <h2><a name="Deepika" >Deepika Kumari </a></h2>
                        <div class="strap">Double Gold medalist at the Commonwealth Games 2010  </div>
                        <div class="session"><b>SESSION: SURVIVORS' TALES </b></div>
                        <div class="fulltext">

						<img id="dyn_img_gallery" src="images/Deepika-Kumary---Subir-3.jpg" style="float: left;" height="110" align="middle" border="1" width="120" />
						<p> The young archer came into the limelight when she won two gold medals, in the individual as well as team recurve events, at the Delhi Commonwealth Games 2010. The most captivating part of her success has been her journey to get there, as this prospective Olympic medalist was born to an autorickshaw driver and a nurse in a village near Ranchi and battled poverty and initial familial apprehensions to get where she did.</p>
						</div>
                        </div>
												<div class="speakers_speak">
                        <h2><a name="Khushbu">Khushbu Sundar  <br />
                        </a></h2>
                        <div class="strap">Actor, Politician</div>
                        <div class="session"><b>SESSION: SURVIVORS' TALES</b></div>
                        <div class="fulltext">

						<img id="dyn_img_gallery" src="images/Khushboo-(1).jpg" style="float: left;" height="110" align="middle" border="1" width="120">


						Chennai -based Indian actor, producer and television host who started her career as a child artist in the 1980s and has to her credit more than 60 successful films in Tamil, Kannada and Malayalam. Known to be outspoken, she faced the ire of self-proclaimed moral policemen when she remarked in an interview that pre-marital sex was acceptable for young girls but not without precautions to prevent pregnancy and sexually transmitted diseases.&nbsp;She was ordered to court and her counsel offered to withdraw the previously issued anticipatory bail petition. Undaunted, she continued to stand by what she said and made quite a political statement when she joined the DMK party of Tamil Nandu and made public her decision to contest the 2011 assembly polls.<br>
                        </div>
                        </div><div class="speakers_speak">
                        <h2><a name="Jitish">Jitish Kallat   <br />
                        </a></h2>
                        <div class="strap">Artist </div>
                        <div class="session"><b>SESSION: THE BATTLE OF THE SEXES</b></div>
                        <div class="fulltext">

						<img id="dyn_img_gallery" src="images/Jitish-Kallat-16.jpg" style="float: left;" height="110" align="middle" border="1" width="120">


						An artist by profession, he received his&nbsp;BFA in painting from the&nbsp;Sir J.J School of Art&nbsp;in Mumbai in 1996, and has ever since established himself in the art world. With twelve shows at venues as varied as Gallery Chemould (Mumbai) to Bose Pacia Modern ( New York), he is amongst one of the most promising young Indian artists, with a style that is slightly autobiographical, he addresses personal relations as well as the ones he has with his ancestory, time, death through his work. Known for regularly exploiting images and materials chanced upon around Mumbia's sprawling metropolis; his work has an inherent spontaneity and a handcrafted aesthetic which he unites through enduring themes such as the relationship between the individual and the masses.<br>
                        </div>
                        </div><div class="speakers_speak">
                        <h2><a name="Sunhil">Sunhil Sippy  <br />
                        </a></h2>
                        <div class="strap">Partner, Highlight films </div>
                        <div class="session"><b>SESSION: THE BATTLE OF THE SEXES</b></div>
                        <div class="fulltext">

						<img id="dyn_img_gallery" src="images/sunhil.jpg" style="float: left;" height="110" align="middle" border="1" width="120">


						A writer and filmmaker he graduated in English from from Georgetown University, Washington DC where he founded The Georgetown Film Society which produced four short films in the two years he was a part of it. Sunhil has written and directed more than 75 episodes of, Disney Club, and has also shot and directed an AIDS awareness television campaign featuring well know celebrities like Aamir Khan, Pooja Bhatt and Javed Akhtar, which has run extensively on Channel V, MTV &amp; ESPN. He also writes and directs all of the in-house promos and commercials for The Walt Disney Company and their licensees in India. He has been directing commercials with Mumbai based production company Highlight Films for the past 10 years, where he is now a partner.<br>
                        </div>
