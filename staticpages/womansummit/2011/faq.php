            <p>India Today Woman Summit & Awards, is a day-long Conference ending with Awards to felicitate Emerging Women Achievers from various fields & a Gala Dinner. 
              <br>
              <br />
              <strong>What is the format of India Today Woman Summit &amp; Awards?<br />
              </strong>The theme of the India Today Woman Summit 2011 is: Changing the Game. <br />
              Altogether there will be 5 sessions, starting with the pre-Lunch Inaugural Keynote Session and ending with the Dinner Keynote Address, Awards & Gala Dinner. The subjects for the Sessions have been chosen to offer relevant, comprehensive and practical insights into issues that affect corporate women. The format of the Summit is designed to promote delegate interaction with the Speakers through the moderated Q & A following each session. <br />
              <br />
              <strong>Who should attend?</strong><br />
Women &amp; Men Professionals from Corporate India, Women &amp; Men Entrepreneurs, Women &amp; Men in Government, from NGOs and Educational Institutions.</p>
            <p><strong>Is there a limit on numbers?</strong><br />
To ensure that the level of interaction with speakers and among delegates is not diluted, a maximum of 250 delegates only will be registered. </p>
            <p><strong>How are the speakers selected?</strong><br />
The speakers have been chosen for their relevance, experience and the insights they bring to the subject, as much as their position. They bring content-rich, thoughtful, incisive and solution-driven understanding of the changing roles of 21st century women and the issues and challenges they face.</p>
            <p><strong>What is the speaking format?</strong><br />
The Summit will have  2 keynote sessions each with a single speaker and  3 panel discussions with four panelists each. The Session will be anchored by eminent personalities with deep knowledge of the subjects. They will be followed by moderated Q &amp; As .<br />
            </p>
