                        <div class="fulltext">

						<img id="dyn_img_gallery" src="images/Anuja-Chauhan-082108-B.jpg" style="float: left;" height="110" align="middle" border="1" width="120" />Formerly Vice President and Executive Creative Director at JWT, one of India's leading advertising agencies, she took up writing fiction as a full-time job and is the author of two bestselling, contemporary romantic comedies, <em>The Zoya Factor</em> (2008) and <em>Battle For Bittora</em> (October 2010). The first novel is set in the glitzy, high profile world of Indian cricket, and the second in the dust and grime of a Lok Sabha election. Hailed as one of the best chicklit authors of the country today, she has comfortably settled into her new job and has no regrets about leaving behind something she was obviously great at. Her first novel, The Zoya Factor won her the <em>Cosmopolitan</em> Magazine&rsquo;s Fun Fearless Female award for literature, 2008, and the India Today Woman award for Woman as Storyteller, 2009.<br>                        
