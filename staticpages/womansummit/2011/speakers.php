                  						<div class="speakers_speak">
                        <h2><a name="Marianne" >Marianne Pearl </a></h2>
                        <div class="strap">Author, Activist </div>
                        <div class="session"><b>DINNER KEYNOTE ADDRESS: THE POWER WITHIN</b></div>
                        <div class="fulltext">

						<img id="dyn_img_gallery" src="images/Pearl_MarianeJPG.jpg" style="float: left;" height="110" align="middle" border="1" width="120">
					
									 
						<p>Mariane Pearl was five months pregnant when her husband, <em>The Wall Street Journal</em> reporter Daniel Pearl was kidnapped and brutally murdered by a militant Islamic fundamentalist group in Pakistan in 2002. Determined not to be broken, she wrote <strong><em>A Mighty Heart: The Brave Life and Death of My Husband Daniel Pearl</em></strong> that introduced the world to Daniel Pearl as he was when he was alive while also providing a heart-breaking first person account of his disappearance and death. It has been translated into 14 languages and turned into a major motion picture starring Angelina Jolie. In a moving and uplifting presentation, Mariane relays a message of courage as a tribute to her husband&rsquo;s life and the values and ideals which they shared. While terrorists did take Daniel from her, they could not make her fearful enough to abandon their principles of hope for the future that she continues to pass on to their young son Adam. A renowned international journalist, Mariane is the recipient of The National Headliners Award for magazine writing, the Time Warner Woman of the Year Award, the White House Project Award, the Internews Award for excellence in international reporting, the Vital Voices Award, the Sciences and Society award in Canada, the El Mundo editorial award in Spain, and the Prix &ldquo;Verite&rdquo; in France for excellence in non-fiction writing. <br />
                        </p>
						<p> Mariane&rsquo;s second book, <strong><em>In Search of Hope</em></strong>, is a collection of profiles of extraordinary women around the world which were collected from her &ldquo;Global Diary&rdquo; columns in <em>Glamour</em> magazine that she continues to write today. The profiles feature women from Cambodia, Cuba, Liberia, Mexico, Turkey, Columbia, Puerto Rico and Uganda and Mariane explores how their courage and accomplishments inspire hope and courage in all of us. <br />
                        </p>
						Marianne continues to gain strength by celebrating life, the values of humanism, and dignity. She is Co-Founder of the Daniel Pearl Foundation created to further the ideals that inspired his life and work and to promote cross-cultural understanding through journalism, music, and innovative communications. She also contributes to <em>The New York Times</em>, <em>Conde Nast Traveler</em>, <em>Self Magazine</em> and others and is at work on a non-fiction book. <br>                        
						</div>
                        </div>
                        
												<div class="speakers_speak">
                        <h2><a name="Padma">Padma Lakshmi </a></h2>
                        <div class="strap">Celebrity, Host of Top Chef, Author </div>
                        <div class="session"><b>SESSION: HER OWN WOMAN</b></div>
                        <div class="fulltext">

						<img id="dyn_img_gallery" src="images/Lakshmi_PadmaJPG.jpg" style="float: left;" height="110" align="middle" border="1" width="120">
					
									 
						The international It Girl and pop phenomenon Padma Lakshmi may be best known as the host of <em>Top Chef</em>, the number 1 food show on cable. But that's just one of her many accomplishments. An example of all you can accomplish in our borderless world, this former supermodel -- India's first -- is also a bestselling and award-winning cookbook author, and is perhaps the most prominent spokesperson in the Western world for ethnic foods. Lakshmi is fluent in five languages, has written for Gourmet and the New York Times, and is currently a producer, adapting Jhumpa Lahiri's Pulitzer Prize winning book, <em>The Interpreter of Maladies</em>, for the big screen.
                        <p>&nbsp; </p>
                        In her popular talks, Padma Lakshmi, a striking and inspiring figure, explores the trajectory of her life: from a student discovered in a cafe, through her formative years breaking into the worlds of fashion, broadcasting and food, to her current place as a powerful media player. She bases her life on one fundamental tenet -- &quot;Be open to all possibilities&quot;--and shares a message of finding your intrinsic talents, skills, and interests to shape a life that is rewarding, both personally and professionally. As one of the few Indian superstars to break the American market, she has stared down stereotypes to overcome discrimination. For her talks, expect large, diverse crowds, an anticipatory, electrifying buzz before her appearance, and a lingering one after.<br>                        
						</div>
                        </div>
                        
												<div class="speakers_speak">
                        <h2><a name="Meera" >Meera H. Sanyal</a></h2>
                        <div class="strap">Chairperson and Country Executive, Royal Bank of Scotland, India  </div>
                        <div class="session"><b>SESSION: FOLLOW YOUR HEART </b></div>
                        <div class="fulltext">

						<img id="dyn_img_gallery" src="images/Meera-Sanyal.jpg" style="float: left;" height="110" align="middle" border="1" width="120">
					
									 
						Is from the generation of women bankers who broke the glass ceiling to make way for more women like them. The Chairperson and Country Executive for ABN AMRO Bank (now RBS) in India, she has been in the banking profession for nearly 25 years.&nbsp;The daughter of a naval officer,&nbsp;after the 26/11 attacks, she took a sabbatical from her banking job and stood for the state elections,&nbsp;unable to remain an armchair critic.&nbsp;As the Chairperson of RBS Foundation, India, she continues to serve the public through microfinance and poverty alleviation programmes.</div>
