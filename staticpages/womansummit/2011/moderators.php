                        <div class="fulltext">

						<img id="dyn_img_gallery" src="images/AP-09-2.jpg" style="float: left;" height="110" align="middle" border="1" width="120">
					
									 
The founding Chairman &amp; Editor-in-Chief of the India Today Group, Aroon Purie heads India&rsquo;s most respected and diversified media conglomerate which publishes a host of magazines and a daily newspaper. The Group has four TV News channels, a Radio channel and owns Thomson Press, India&rsquo;s largest commercial printing plant.&nbsp; An alumnus of the London School of Economics and a qualified Chartered Accountant, Purie has served on the Board of many prestigious institutions in India and abroad and is presently Chairman of the Board of the International Federation of the Periodical Press (FIPP). He is the recipient of numerous awards for his outstanding contribution to Journalism in India and was awarded the Padma Bhushan, amongst India&rsquo;s highest civilian honours, in 2001 .<br>                        
