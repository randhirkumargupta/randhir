                  						<div class="speakers_speak">
                        <h2><a name="Priya" >Priya Paul<br />
                        </a></h2>
                        <div class="strap">Chairperson, The Park Hotels, India </div>
                        <div class="session"><b>SESSION: TAKING CHARGE</b></div>
                        <div class="fulltext">

						<img id="dyn_img_gallery" src="images/Priya-Paul1-vm-310804_120x110.jpg" style="float: left;" height="110" align="middle" border="1" width="120">
					
									 
						Priya Paul is the Chairperson of Apeejay Surrendra Park Hotels and is a prominent woman entrepreneur in India. Her contributions and innovations in the hospitality industry as well as commerce have repeatedly been recognised. She was in 2006 recognized by Forbes Online as one of India&rsquo;s 100 most powerful businesswomen. The Federation of Hotels and Restaurants Association of India conferred on her the Young Entrepreneur of the Year award (1999-2000) and UK&rsquo;s Tatler magazine rated The Park (Bangalore, India), part of her Group, as one of the 101 Best Hotel Worldwide in 2003. She took charge of the then-three group hotel chain at the age of 23 after her father Surrendra Paul was gunned down by ULFA militants in Assam. Today The Park Hotels are known as the pioneers in the concept of premium boutique hotels and are located in Bangalore, Chennai, New Delhi, Navi Mumbai, Kolkata, Hyderabad and Vishakapatnam. There are plans to open properties in the Eastern Metropolitan Bypass, Kolkata, Pune and Jaipur. <br>                        
						</div>
                        </div>
                        <div class="speakers_speak">
                        <h2><a name="Tara" >Tara Thiagarajan</a></h2>
                        <div class="strap">Chairperson of Madura Micro Finance Ltd.</div>
                        <div class="session"><b>SESSION: START- UP SMARTS: WOMAN AS ENTREPRENEUR </b></div>
                        <div class="fulltext">

						<img id="dyn_img_gallery" src="images/tara.jpg" style="float: left;" height="110" align="middle" border="1" width="120" />Tara Thiagarajan is the Chairperson of Madura Micro Finance Ltd., a microfinance institution serving over 400,000 rural women in Tamil Nadu with unique initiatives to build their aspirations and capacity as entrepreneurs. Together with its partner institution Microcredit Foundation of India, Madura has been recognized by Forbes as one of the most cost efficient microfinance providers worldwide, and provides loans to rural women at one of the lowest rates on the market. Tara Thiagarajan holds a BA in Mathematics from Brandeis University, an MBA from the Kellogg School of Management at Northwestern and a Ph.D. in Neuroscience from Stanford University. In her spare time she is a neuroscientist working on how distributed electrical activity in the brain can give rise to thought.</div>
                        </div>
												<div class="speakers_speak">
                        <h2><a name="Vandana">Vandana Luthra <br />
                        </a></h2>
                        <div class="strap">Founder and Mentor, VLCC Health Care Ltd.</div>
                        <div class="session"><b>SESSION: START-UP SMARTS: WOMAN AS ENTREPRENEUR</b></div>
                        <div class="fulltext">

						<img id="dyn_img_gallery" src="images/Vandana-Luthra_120x110.jpg" style="float: left;" height="110" align="middle" border="1" width="120">
					
									 
						Vandana Luthra is the founder of VLCC, India's best known wellness brand. Established in 1989, VLCC is currently present across over 225 locations in 90 cities and has operations in seven countries, including India, Nepal, UAE, Bahrain, Oman, Kuwait and Qatar. <br />
						<br />
						<p>Ms. Luthra believes in three ideologies: transforming future, transforming self and spreading happiness. It is this that forms the backbone of the company that operates a chain of outlets offering weight management and beauty solutions. The VLCC group is also involved in manufacturing and retailing of herbal/natural cosmetics and managing the VLCC Institute of Beauty, Health &amp; Management, a chain of 48 vocational training centres. Ms. Luthra is closely associated with several initiatives for empowerment of women. The Department of Industrial Policy &amp; Promotion, Ministry of Commerce &amp; Industry also made her a member of the taskforce on women entrepreneurs. She is also a trustee of the National Childrens Fund constituted by the Government of India under the aegis of the Ministry of Women &amp; Child Development.</p>
						<br>                        
						</div>
