                        <h2><a name="Vinita" >Vinita Kamte</a></h2>
                        <div class="strap">Activist, Author </div>
                        <div class="session"><b>SESSION: TAKING CHARGE</b></div>
                        <div class="fulltext">

						<img id="dyn_img_gallery" src="images/VINITA-KAMTE_120x110.jpg" style="float: left;" height="110" align="middle" border="1" width="120">
					
									 
						Vinita Kamte is the wife of Ashok Kamte, additional commissioner of police, Mumbai East, who was gunned down by terrorists in the 26/11 Mumbai attacks near Cama Hospital. Ms. Kamte, a labour lawyer by training went on to write a book<em> To The Last Bullet</em>, which was co-authored by senior journalist Vinita Deshmukh. The book is a chronicle of Ms. Kamte&rsquo;s search for truth and her husband&rsquo;s death that turned into a struggle against the establishment. She used the Right to Information Act to obtain documents that would help in answering the many questions she had about that night. </div>
