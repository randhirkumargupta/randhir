                  						<div class="speakers_speak">
                        <h2><a name="Aishwarya" >Aishwarya Rai Bachchan</a></h2>
                        <div class="strap">Actor</div>
                        <div class="session"><b>SESSION: NEW BEGINNINGS, NEW FRONTIERS</b></div>
                        <div class="fulltext">

						<img id="dyn_img_gallery" src="images/aishwarya_120x110.jpg" style="float: left;" height="110" align="middle" border="1" width="120">
					
									 
						Aishwarya Rai Bachchan is actor and former Miss World who has during her career acted in over 40 movies in Hindi, English, Tamil and Bengali. This includes the international productions of <em>Bride and Prejudice</em>, <em>Mistress of Spices</em>, <em>Provoked</em>, <em>The Last Legion </em>and more recently <em>Pink Panther 2</em>. She has also worked in critically acclaimed films like <em>Devdas</em>, <em>Guru </em>and <em>Jodhaa Akbar</em>. In 2009, Ms. Rai was awarded the Padma Shri for her contribution to Indian cinema and was chosen for the Order of France. In June 2009 she was declared the Female Star of the Decade at the 10th International Indian Film Academy Awards held in Macau. In 2003, she became the first Indian female actor to be jury member at the Cannes Film Festival. <br>                        
						</div>
                        </div>
                        
												<div class="speakers_speak">
                        <h2><a name="Akshata">Akshata Murty</a></h2>
                        <div class="strap">Entrepreneur</div>
                        <div class="session"><b>SESSION: TAKING CHARGE</b></div>
                        <div class="fulltext">

						<img id="dyn_img_gallery" src="images/Infosys-Chairman-N-R-Narayana-Murthy%27s-daughter-Akshata_120x110.jpg" style="float: left;" height="110" align="middle" border="1" width="120">
					
									 
						Akshata Murty is Infosys' founder-chairman N.R. Narayana Murty's daughter. She has worked as a consultant with Deloitte as well as Unilever before doing her MBA from Stanford. After working for Siderian Ventures, she has now entered the world of fashion with the launch of her own brand of clothing Akshata. The essence behind the retail concept is to create an affordable yet luxurious fashion brand that focuses on traditional art forms from around the world and infusing them on western clothing while ensuring the revival of local crafts and building a sustainable livelihood for artisans. The brand aims at reaching out to those women who are looking for unique approaches to fashion without compromising on style. The concept also aims at using the clothing line to promote cultural awareness among its target audience.<br>                        
						</div>
                        </div>
                        
												<div class="speakers_speak">
                        <h2><a name="Bridget" >Bridget Brennan</a></h2>
                        <div class="strap">Author &amp; Founder Female Factor</div>
                        <div class="session"><b>SESSION: WHY SHE BUYS </b></div>
                        <div class="fulltext">

						<img id="dyn_img_gallery" src="images/Bridget-Brennan_120x110.jpg" style="float: left;" height="110" align="middle" border="1" width="120">
					
									 
						Bridget Brennan is the founder of Female Factor, a marketing and sales consultancy that advises companies on how to create products, services and sales environments that appeal to women. She is also the author of Why She Buys: The New Strategy for Reaching the World&rsquo;s Most Powerful Consumers which was published in July 2009 and gives readers the tools to create, market and sell products and services to women. Ms. Brennan is a pioneer in developing female-focused strategies and techniques, and has worked with major corporations to put them into practice. She has created strategic communications programs for clients like Whirlpool Corporation, Johnson &amp; Johnson, Pizza Hut, Colgate-Palmolive, United Airlines and AT&amp;T, among others. Previous to founding Female Factor, Brennan created the &ldquo;Speaking Female&rdquo; marketing practice of Zeno, a Daniel J. Edelman marketing-communications firm. She is a graduate of Texas A&amp;M University and based in Chicago.</div>
                        </div>
                        
												<div class="speakers_speak">
                        <h2><a name="Camellia" >Camellia Panjabi</a></h2>
                        <div class="strap">Author, Creator Masala Zone Restaurant Chain </div>
                        <div class="session"><b>SESSION: START-UP SMARTS: WOMAN AS ENTREPRENEUR</b></div>
                        <div class="fulltext">
						<img id="dyn_img_gallery" src="images/camellia_panjabi_120x110.jpg" style="float: left;" height="110" align="middle" border="1" width="120">

					
									 
						Camellia Panjabi, author and one of the creators behind the successful international Masala Zone restaurant chain, has been credited with making regional Indian cuisine a favourite in the western world. She is a shining example of professionalism and outstanding managerial skills in the hospitality business. Amaya, one of her six London restaurants, was the first to receive a Michelin star. Ms. Panjabi was born in Mumbai and studied economics at Cambridge before going to become the marketing director at Taj Hotels. She is also one of the directors of Masala World that owns the legendary Chutney Mary, Veeraswamy, Amaya and the Masala Zone restaurants in London. Veeraswamy, one of UK&rsquo;s oldest Indian restaurants, was acquired by the group in 1997 and has been named on of the 10 Best Destination &amp; Special Restaurants in the World by National Geographic. Amaya, which was launched in 2004, went on to win two prestigious awards in the UK in the same year &ndash; The Tio Pepe ITV Award for the Best Restaurant of the Year as well as the Best New Restaurant of the Year.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <br>                        
						</div>
