            <p>India Today Woman Summit &amp; Awards, is a day-long Conference ending with Awards to felicitate Emerging Women Achievers from various fields &amp; a Gala Dinner. 
              <br>
              <br />
              <strong>Inaugural Keynote Session<br />
              IS LEADERSHIP IN A WOMAN&rsquo;S DNA?<br />
              Session time: </strong>12.00 - 13.00<br />
              What does it take to become a superwoman? To combine high EQ with a sharp IQ? What does a woman need to lead the world, to solve complex corporate problems while successfully managing human relationships? The answers are with a man whose far-reaching vision and uncanny genius for tapping into talent, made him philosopher, guide and mentor to a whole generation of women who have now made it to the highest echelons of the corporate world. Today his prot&eacute;g&eacute;s sit in practically every board room across the country. K.V. Kamath hits straight on a sticky wicket.</p>
            <p><strong>START-UP SMARTS: WOMAN AS ENTREPRENEUR <br />
  Session time:</strong> 14.00 - 15.30<br />
            It starts with the birth of an idea, a dream that is waiting to turn to reality. But the challenges an entrepreneur faces are many and not every challenge is insurmountable. Four women have proved that. Each woman has a different story to tell. Whether it's Camellia Panjabi, an author and restaurateur who succeeded in giving Indian cuisine an international flavour. Vandana Luthra, a woman whose name is now synonymous with beauty. Sunita Maheshwari, a paediatric cardiologist who reinvented the medical outsourcing business in India and Tara Thiagarajan who, as Chairperson of Madura Micro Finance, has built a unique institution serving over 400,000 rural women in Tamil Nadu to build their capacity as entrepreneurs. They discuss how they started their ventures, the challenges they faced and how they overcame them. </p>
            <p><strong>WHY SHE BUYS <br />
  Session time:</strong> 15.45 - 16.45<br />
              What makes a woman buy? What does she look for? And what are the perfect strategies that would call out to a woman? Hear it from the CEO of Female Factor who has demystified the buyer and offers some insider knowledge on marketing to women. A pioneer in marketing and sales strategies that appeal to women, she is also the author of a best-selling book and consultant in female-focused strategies, product services and sales environments. She shares her insight on the emotional selling preposition that never fails to appeal to a woman buyer.</p>
            <p><strong>TAKING CHARGE<br />
  Session time: </strong>17.15 - 18.45<br />
              Every woman walks through fire in order to bring change to the world. It could be to carve a niche for herself in the corporate world like Naina Lal Kidwai did or to give a family-run business a dynamic new skin, like Priya Paul. It could also be a search for answers to questions that remain hidden, a challenge Vinita Kamte accepted. Or it could be an attempt to step out of the box to create a youthful, new definition to a business like Akshata Murty. Each of these women tells her story. Listen to how they surmounted challenges and blazed new trails to show that women are not the weaker sex, or even the fairer sex &ndash; but the equal sex.</p>
            <p><strong>NEW BEGINNINGS, NEW FRONTIERS<br />
  Session time:</strong> 19.30 - 20.15<br />
              She is not new to taking charge of her world, walking the road less travelled and crafting her own rules. As if being a beauty queen and ruling the box office across India were not enough, Aishwarya Rai Bachchan also chose to make a mark in the international arena. Today, she is the face and name that has put India on the global map. She is the perfect blend of the modern 21<sup>st</sup> century woman who is still rooted in classic Indian traditions. She has proved beyond a doubt that beauty isn&rsquo;t necessarily skin deep. This is a woman who believes in challenging the norm and pushing boundaries in hide-bound Bollywood.<br />
            </p>
