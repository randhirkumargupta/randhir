            <p>The INDIA TODAY WOMAN SUMMIT &amp; AWARDS 2010 aims to acknowledge the contribution of the modern Indian woman to the work force.</p>
            <p>The forum provides a day long interaction between highly qualified and successful women with those who are ambitious and striving for the vision, strategies and skills needed to make their mark in today's complex world. </p>
            <p>Top Women Professionals from Corporate India, Women Entrepreneurs, Women in Government, from NGOs and Educational Institutions, will all have a platform at this unique gathering.</p>
            <p>The Summit is for Women aspiring towards Leadership, in whatever field they choose--- to power them to deal with challenges and take charge of their professional and home lives. 
