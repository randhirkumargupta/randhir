                  						<div class="speakers_speak">
                        <h2><a name="AroonPurie" >Aroon Purie</a></h2>
                              <div class="strap">Editor-in-Chief, India Today</div>
                        <div class="session"><b>SESSION: IS LEADERSHIP IN A WOMAN'S DNA?</b></div>
                        <div class="fulltext">

						<img id="dyn_img_gallery" src="images/ap-08-1_120x110.jpg" style="float: left;" height="110" align="middle" border="1" width="120">
					
									 
						The founding Chairman &amp; Editor-in-Chief of The India Today Group, Aroon Purie heads India&rsquo;s most respected and diversified media conglomerate which publishes a host of magazines and a daily newspaper. The Group has four leading TV news channels, a radio channel and owns Thomson Press, India&rsquo;s largest commercial printing plant. An alumnus of the London School of Economics and a qualified chartered accountant, Purie has served on the boards of many prestigious institutions in India and abroad and is presently Chairman of the International Federation of the Periodical Press (FIPP). He has been instrumental in changing the face of journalism in India and is the recipient of numerous awards for his outstanding contribution in both print and television. He was awarded the Padma Bhushan - amongst India&rsquo;s highest civilian honors - by the President of India in 2001.<br>                        
						</div>
                        </div>
                        
												<div class="speakers_speak">
                        <h2><a name="KoelPurie">Koel Purie</a></h2>
                        <div class="strap">Actor &amp; TV Host </div>
                        <div class="session"><b>SESSION: NEW BEGINNINGS, NEW FRONTIERS</b></div>
                        <div class="fulltext">

						  <p><img id="dyn_img_gallery" src="images/Koel-01_120x110.jpg" style="float: left;" height="110" align="middle" border="1" width="120">
					  
									 
						  Koel Purie is actor and talk show host. She made her debut with the critically acclaimed <em>Everybody Says I&rsquo;m Fine</em>, actor Rahul Bose&rsquo;s directorial venture and also starred with Irrfan Khan in <em>Road to Ladakh</em>. Ms. Purie was recently seen in <em>Rock On!!</em> and <em>Secrets of Seven Sounds</em>. She hosts the popular talk show, <em>On the Couch With Koel </em>for the news channel <em>Headlines Today</em>.<br>                        
						        </p>
						  </div>
                        </div><div class="speakers_speak">
                        <h2><a name="Siddharth" >Siddharth Dhanvant Shanghvi</a></h2>
                        <div class="strap">Author</div>
                        <div class="session"><b>SESSION: <strong>TAKING CHARGE</strong><br />
                        </b></div>
                        <div class="fulltext">
						<img id="dyn_img_gallery" src="images/Siddharth-Dhanvant-022709-RS-02_120x110.jpg" style="float: left;" height="110" align="middle" border="1" width="120">

					
									 
						Siddharth Dhanvant Shanghvi's debut novel, <em>The Last Song of Dusk</em>, won the Betty Trask Award in the UK, the Premio Grinzane Cavour in Italy, and was nominated for the IMPAC Prize. Translated into 12 languages, <em>The Last Song of Dusk </em>was an international bestseller. Shanghvi has been voted: <em>India Today</em>'s 50 Most Powerful Young Indians; <em>Sunday Times </em>UK: The Next Big Thing; <em>New Statesmen </em>UK: India's Ten Bright Lights; <em>Elle </em>Magazine's 50 Most Stylish People; and <em>La Stampa</em>, Italy: World's 10 Best Dressed Men. Shanghvi's new novel, <em>The Lost Flamingoes of Bombay</em>, was nominated for the Man Asian Literary Prize 2008 and was a number one bestseller. He lives in Bombay. <br>                        
						</div>
                        </div>
						<div class="speakers_speak">
                        <h2><a name="Sonal" >Sonal Agrawal</a></h2>
                        <div class="strap">Chief Executive Officer, Accord Group India</div>
                        <div class="session"><b>SESSION: START- UP SMARTS: WOMAN AS ENTREPRENEUR</b></div>
                        <div class="fulltext">
						<img id="dyn_img_gallery" src="images/sonal.jpg" style="float: left;" height="110" align="middle" border="1" width="120">

					
									 
						An MBA from London Business School, Sonal Agrawal has had over 15 years of experience in executive search and selection in India and started Accord Group India in 2005, after a stint with ABC Consultants. Apart from client engagement across sectors, she leads the Professional &amp; Financial Services practice. Ms. Agrawal serves on the global management committee, and is the Regional Head &ndash; Asia Pacific for AltoPartners, a global partnership of executive search firms in 24 countries. She has served on the boards of ABC Consultants India, ABC Consultants Singapore and Watson Wyatt India, and is a member of the YPO, (Young Presidents Organization) a peer network of 20,000 global leaders in 100 nations. She was named by Businessweek as one of the 150 Most Influential Headhunters in the World, 2008.<br>                        
						</div>
                        </div>
                        
												<div class="speakers_speak">
                        <h2><a name="Vikram" >Vikram Sakhuja</a></h2>
                        <div class="strap">CEO, South Asia, Group M Media India Pvt. Ltd.</div>
                        <div class="session"><b>SESSION: WHY SHE BUYS</b></div>
                        <div class="fulltext">
						<img id="dyn_img_gallery" src="images/Vikram_Sakhuja_GroupM_CEO_0028_120x110.jpg" style="float: left;" height="110" align="middle" border="1" width="120">

					
									 
						Vikram Sakhuja is CEO, South Asia for GroupM Media India Pvt. Ltd. Before this he was COO South Asia at GroupM where he lead WPP&rsquo;s media agencies like MindShare, Maxus, Mediaedge:CIA, Mediacom and Motivator. The aim was to deliver competitive advantage to clients. He was MD of MindShare Fulcrum till 2004, after which he took additional charge of MindShare India. He has been instrumental in championing communication based, channel-neutral planning and brand activation into MindShare. In 2005, MindShare was voted the Media Agency of the Year in India. He is a graduate of India Institute of Management, Calcutta and went on to join Coca-Cola India as their Marketing Manager Brands.<br>                        
						</div>
