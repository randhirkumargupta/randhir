                  						<div class="speakers_speak">
                        <h2><a name="Kundapur" >Kundapur Vaman Kamath</a></h2>
                        <div class="strap">Non-Executive Chairman, ICICI Bank Ltd. </div>
                        <div class="session"><b>SESSION: IS LEADERSHIP IN A WOMAN'S DNA</b></div>
                        <div class="fulltext">

						<img id="dyn_img_gallery" src="images/KV-Kamath_120x110.jpg" style="float: left;" height="110" align="middle" border="1" width="120">
					
									 
						K.V. Kamath is former managing director &amp; CEO, ICICI Bank Ltd., recipient of the Padma Bhushan and a mechanical engineer by training. A graduate of the Indian Institute of Management, Ahmedabad, Mr Kamath joined ICICI in 1971. It was under his leadership that the ICICI Group transformed itself into a diversified, technology-driven financial services group with leadership positions across banking, insurance and asset management in India, and an international presence. Mr Kamath created not just a new financial institution but a new benchmark for Indian institutions by enabling women executives to render the glass ceiling irrelevant. It is a testimony to his faith that India&rsquo;s largest private banks are run by professionals mentored by him. He retired in April 2009 and subsequently took up the position of Chairman of the Board, ICICI Bank Ltd. He has been conferred with an honorary PhD by the Banaras Hindu University. He is a member of the Board of the Institute of International Finance, a Director on the Board of Infosys Technologies and a member of the Board of Governors of the Indian Institute of Management, Ahmedabad. <br>                        
						</div>
                        </div>
                        
							<div class="speakers_speak">
                        <h2><a name="Sunita">Sunita Maheshwari</a></h2>
                        <div class="strap">Paediatric Cardiologist and co-founder Teleradiology Solutions</div>
                        <div class="session"><b>SESSION: START-UP SMARTS: WOMAN AS ENTREPRENEUR</b></div>
                        <div class="fulltext">

						<img id="dyn_img_gallery" src="images/sunita120.jpg" style="float: left;" height="110" align="middle" border="1" width="120" />A paediatric cardiologist, Sunita Maheshwari returned to India with her husband to start Teleradiology Solutions that revolutionised the concept of outsourcing teleradiology solutions. From a two-person operation in 2002, the company now looks at over 2.6 million scans with 350 employees all over the world. It examines scans from 75 hospitals in the US and 11 centres in Singapore with accreditation from the US Joint Commission of Accreditation of Healthcare Organisati9ons and is the first such organisation outside Singapore to be certified by the country&rsquo;s Ministry of Health.<br>                        
						</div>
                        </div>
                        
												<div class="speakers_speak">
                        <h2><a name="Naina">Naina Lal Kidwai</a></h2>
                        <div class="strap">Group GM &amp; Country Head, HSBC (India) </div>
                        <div class="session"><b>SESSION: TAKING CHARGE</b></div>
                        <div class="fulltext">

						<img id="dyn_img_gallery" src="images/Naina-Lal-Kidwai_120x110.jpg" style="float: left;" height="110" align="middle" border="1" width="120">
					
									 
						Naina Lal Kidwai is group general manager and country head of HSBC Group Companies in India. Prior to this she was deputy chief executive officer HSBC and managing director and vice chairman of HSBC Securities and Capital Markets. As deputy chief executive officer, she was responsible for the securities and custody business of HSBC and during that period the bank&rsquo;s custody business grew by over 200 per cent. She has also been vice chairman at JM Morgan Stanley and helped start up the firm&rsquo;s investment banking operations on joining Morgan Stanley in April 1994 and subsequently initiated the merger of JM Financial and Morgan Stanley in India in 1998. She has an MBA from the Harvard Business School and was awarded the Padma Shri in 2007 for her work in the field of trade and industry. She was ranked 34th in the list of the world&rsquo;s top 50 businesswomen announced by The Wall Street Journal in 2004 and ranked third in Asia by<em> Fortune </em>Magazine in their first ever listing of the world&rsquo;s top women in business in Asia in 2000. </div>
