                      <td class="normal" colspan="6" height="50" width="97%"><strong> 
                        </strong><strong>What is the conference format? <br />
                        </strong>Conclave 2009 has 12 sessions starting with an Inaugural Keynote on March 6, 2009 and ending with a Dinner Keynote on March 7, 2009, with two full days of sessions on March 6 &amp; 7. The session subjects have been chosen to offer leading edge, relevant, comprehensive policy and practical insights. The format of the Conclave is designed to promote interaction. There are no parallel sessions. Every session includes a Q&amp;A in which delegates can interact with the speakers. <br />
                        <br>                      </td>
                    </tr>
                    <tr> 
                      
                      <td class="normal" colspan="6" height="50"><strong>                        </strong><strong>Who should attend? <br />
                      </strong>CEOs, top managers, strategists, analysts and top public officials. <strong>                      </strong><br>   <br>                   </td>
                    </tr>
                    <tr> 
                     
                      <td class="normal" colspan="6" height="50"><strong>                          <strong> Is there a limit on numbers?</strong></strong><br>
Yes. A maximum of 300 delegates to ensure that peer-level interaction is not diluted. This way, delegates will have more opportunity and time to interact with one another and speakers, key government officials and analysts.<br>
                          <br>                      </td>
                    </tr>
                    <tr> 
                     
                      <td class="normal" colspan="6" height="50"><strong>                          <strong> How are the speakers selected?</strong></strong><br>
They have been chosen for their expertise, international standing &amp; practical experience of the subject. They bring content-rich, thoughtful, incisive and solution-driven understanding of Indian, Asian and global issues.<br />
                          <br>                      </td>
                    </tr>
                    <tr> 
                     
                      <td class="normal" colspan="6" height="50"><strong>                        What is the speaking format?</strong><br>
The India Today Conclave provides increased speaker talktime and interaction with the delegates. The average session is 50 minutes, featuring two speakers followed by a Q&amp;A for approximately 15 minutes. Each session is chaired by an eminent authority on the subject of the session.                      </td>
